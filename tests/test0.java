public class MyClass {
    

    // function to check if a given string is a palindrome
    public static void main() {
        // remove whitespace and convert to lowercase
        int i;
        int n = 10;
        int c[] = new int [10];
        int x;
        for(i = 0; i < n; i++) {
            c[i] = i + 1;   
            x = c[i] + 0;
            System.out.println(x);
        }
        x = c[2] + c[3];
        System.out.println(x);
       
    }
}
